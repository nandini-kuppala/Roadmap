export function getDayPlan(dayNumber: number): DayPlan | undefined {
  return SIXTY_DAY_PLAN.find(d => d.dayNumber === dayNumber);
}

export function getDayPlanByDate(startDate: Date, targetDate: Date): DayPlan | undefined {
  const msPerDay = 1000 * 60 * 60 * 24;
  const dayNumber = Math.floor((targetDate.getTime() - startDate.getTime()) / msPerDay) + 1;
  if (dayNumber < 1 || dayNumber > 60) return undefined;
  return getDayPlan(dayNumber);
}
